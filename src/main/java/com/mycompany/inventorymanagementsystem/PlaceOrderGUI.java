    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        Supplier supplier = null;
        String supplierName = supplierNameTxtFld.getText();
        if (supplierName.equalsIgnoreCase("AINUL MARDHIAH")) {
            supplier = new Supplier("AINUL MARDHIAH", "ainul@gmail.com", "RC1");
        } 
        else if (supplierName.equalsIgnoreCase("LEE YONG YI")) {
            supplier = new Supplier("LEE YONG YI", "lee@gmail.com", "RC2");
        } 
        else if (supplierName.equalsIgnoreCase("AIDA NADHIRAH")) {
            supplier = new Supplier("AIDA NADHIRAH", "aida@gmail.com", "RC3");
        } 
        else if (supplierName.equalsIgnoreCase("SYAMIMI")) {
            supplier = new Supplier("SYAMIMI", "im@gmail.com", "RC4");
        } 
        else {
            JOptionPane.showMessageDialog(rootPane, "Supplier not found.");
            return;
        }
        
        order = new Order(productTxtFld.getText(), 
                      Integer.parseInt(quantityTxtFld.getText()), 
                      supplier, 
                      Double.parseDouble(priceTxtFld.getText()), 
                      deliveryDateTxtFld.getText(), 
                      "Pending");
        
        addToList(order);
        clearTextField();
        order.displayDetails();
        
        this.setVisible(false);
        new MainPageGUI().setVisible(true);
    }//GEN-LAST:event_submitBtnActionPerformed

