    public void addToList(Order order) {
        for (int i = 0; i < order.getOrderList().length; i++) {
            if (order.getOrderList()[i] == null) {
                order.getOrderList()[i] = order;
                JOptionPane.showMessageDialog (rootPane, "Order added successfully.");
                return;
            }
        }
        JOptionPane.showMessageDialog (rootPane, "List is full. Unable to add the order.");
    }
    
