    private void populateOrderList() {
        Order[] orders = Order.getOrderList();
        if (orders != null) {
            for (int i = 0; i < orders.length; i++) {
                if (orders[i] != null) {
                    orderNames[i] = orders[i].getProduct();
                }
            }
            orderList.setListData(orderNames);
        }
    }
    
    private void setupOrderListSelectionListener() {
        orderList.addListSelectionListener(new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    int selectedIndex = orderList.getSelectedIndex();
                    if (selectedIndex != -1) {
                        Order selectedOrder = Order.getOrderList()[selectedIndex];
                        if (selectedOrder != null) {
                            displayDetails(selectedOrder);
                        }
                    }
                }
            }
        });
    }
    
    public void addToList(Order order) {
        for (int i = 0; i < order.getOrderList().length; i++) {
            if (order.getOrderList()[i] == null) {
                order.getOrderList()[i] = order;
                JOptionPane.showMessageDialog (rootPane, "Order added successfully.");
                return;
            }
        }
        JOptionPane.showMessageDialog (rootPane, "List is full. Unable to add the order.");
    }
    
    private void displayDetails(Order order) {
        this.order = order;
        OrderIDTxt1.setText(String.valueOf(order.getOrderId()));
        NewPNTxt.setText(order.getProduct());
        QtyTxt.setText(String.valueOf(order.getQuantity()));
        PriceTxt.setText(String.valueOf(order.getPrice()));
        DDateTxt.setText(order.getDeliveryDate());
    }

    private void clearTextField() {
        OrderIDTxt1.setText("");
        NewPNTxt.setText("");
        QtyTxt.setText("");
        PriceTxt.setText("");
        DDateTxt.setText("");
    }

