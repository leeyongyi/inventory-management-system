    private void SubmitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitBtnActionPerformed
        Order selectedOrder = this.order;
        if (selectedOrder == null) {
            JOptionPane.showMessageDialog(rootPane, "Please select an order from the list.");
            return;
        }

        String newProductName = NewPNTxt.getText();
        int newQuantity = Integer.parseInt(QtyTxt.getText());
        double newPrice = Double.parseDouble(PriceTxt.getText());
        String newDeliveryDate = DDateTxt.getText();

        selectedOrder.setProduct(newProductName);
        selectedOrder.setQuantity(newQuantity);
        selectedOrder.setPrice(newPrice);
        selectedOrder.setDeliveryDate(newDeliveryDate);

        JOptionPane.showMessageDialog(rootPane, "Order details updated successfully.");

        clearTextField();
        this.setVisible(false);
        new MainPageGUI().setVisible(true);
    }//GEN-LAST:event_SubmitBtnActionPerformed

