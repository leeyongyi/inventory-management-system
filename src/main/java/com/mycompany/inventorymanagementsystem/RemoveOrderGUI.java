    private void populateOrderList() {
        Order[] orders = Order.getOrderList();
        if (orders != null) {
            int nonNullCount = 0;
            for (Order order : orders) {
                if (order != null) {
                    nonNullCount++;
                }
            }

            String[] orderNames = new String[nonNullCount];

            int index = 0;
            for (Order order : orders) {
                if (order != null) {
                    orderNames[index++] = String.valueOf(order.getOrderId());
                }
            }

            orderList.setListData(orderNames);
        }
    }

    private boolean removeOrder(int orderId) {
        Order[] orders = Order.getOrderList();

        for (int i = 0; i < orders.length; i++) {
            if (orders[i] != null && orders[i].getOrderId() == orderId) {
                orders[i] = null;
                return true;
            }
        }

        return false;
    }
    
