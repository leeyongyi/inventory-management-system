    private void SubmitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitBtnActionPerformed
         String orderIdText = OrderIDTxt.getText();
        if (orderIdText.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please select an order.");
            return;
        }
        int orderIdToRemove = Integer.parseInt(OrderIDTxt.getText());
        boolean removed = removeOrder(orderIdToRemove);

        if (removed) {
            JOptionPane.showMessageDialog(rootPane, "Order removed successfully.");
            clearTextField();
            populateOrderList();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Order ID not found.");
        }
        
        this.setVisible(false);
        new MainPageGUI().setVisible(true);
    }//GEN-LAST:event_SubmitBtnActionPerformed

